/** The URL parameter names */
export enum UrlParameter {
  /** Stores the current opened tab on the HOME page */
  TAB_INDEX = 'tabIndex',
  /** Stores if in debug mode */
  DEBUG = 'debug',
  /** Stores the currently selected spectator */
  SPECTATOR = 'spectator',
  /** Stores the currently selected participant ID */
  SELECTED_PARTICIPANT_ID = 'participant_id',
  /** Stores the currently selected participant type */
  SELECTED_PARTICIPANT_TYPE = 'participant_type',
  /** Stores the currently selected smart contract ID */
  SELECTED_SMART_CONTRACT_ID = 'smart_contract_id',
}
