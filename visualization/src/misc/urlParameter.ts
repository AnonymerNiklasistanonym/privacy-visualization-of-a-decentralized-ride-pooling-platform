/** The URL parameter names */
export enum UrlParameter {
  /** Stores the current opened tab on the HOME page */
  TAB_INDEX = 'tab',
  /** Stores if in debug mode */
  DEBUG = 'debug',
  /** Stores the spectator */
  SPECTATOR = 'spectator',
  /** Stores the currently selected participant */
  SELECTED_PARTICIPANT = 'selected_participant',
  /** Stores the currently selected smart contract ID */
  SELECTED_SMART_CONTRACT_ID = 'smart_contract_id',
  /** Stores the currently pinned participants */
  PINNED_CUSTOMERS = 'pinned_customers',
  /** Stores the currently pinned participants */
  PINNED_RIDE_PROVIDERS = 'pinned_ride_providers',
}
